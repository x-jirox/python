#OPERADORES ARITMETICOS

#operaciones
print(5 + 3)
print(5 - 3)
print(5 * 3)
print(5 / 2)
print("-----------------------------")

#modulos
print(10 % 3)
print(17 // 3)
print(5 ** 2)
print("-----------------------------")



#union de cadenas
print("Hola " + "Python")

#forzar que se use como string con str
print("Hola " + str(5))

#multiplica la cadena
print("Hola " * 5)
print("Hola " * (2**2))

#uso de variable con operaciones
my_float = 2.5 * 2
print("Hola "* int(my_float))
print("-----------------------------")





#OPERADORES COMPARATIVOS

#comparacion de integers
print(5 < 3)
print(5 > 3)
print(5 <= 3)
print(5 >= 3)
print(5 == 3)
print(5 != 3)

print("-----------------------------")





#OPERADORES LOGICOS

print(3 < 5 and "Hola" < "Python")
print(3 > 5 or "Hola" > "Python")
print(not(3 > 4) )